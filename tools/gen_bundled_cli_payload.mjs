#!/usr/bin/env node
// Generate the embedded Wasm payload module consumed by twk_cli_bundled.mjs.

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT_DIR = resolve(__dirname, "..");

const bootWasm = resolve(process.env.BOOT_WASM || resolve(ROOT_DIR, "target/boot.wasm"));
const bridgeWasm = resolve(process.env.BRIDGE_WASM || resolve(ROOT_DIR, "tools/bridge.wasm"));
const outPath = resolve(process.env.TWK_CLI_PAYLOAD || resolve(ROOT_DIR, "target/twk_cli_payload.mjs"));

function toBase64(path) {
  return readFileSync(path).toString("base64");
}

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(
  outPath,
  `// Generated by tools/gen_bundled_cli_payload.mjs. Do not edit.\n` +
    `export const BOOT_WASM_BASE64 = ${JSON.stringify(toBase64(bootWasm))};\n` +
    `export const BRIDGE_WASM_BASE64 = ${JSON.stringify(toBase64(bridgeWasm))};\n`,
);

console.log(`Wrote bundled CLI payload: ${outPath}`);
console.log(`  boot:   ${bootWasm}`);
console.log(`  bridge: ${bridgeWasm}`);
