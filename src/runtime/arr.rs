use crate::runtime::types::*;
use crate::wasm::ir::*;

/// Persistent bit-partitioned trie vector with branching factor 32.
///
/// Stage0 mirror of `boot/compiler/codegen/runtime/arr.tw`.
const B: i32 = 5;
const BF: i32 = 1 << B;
const MASK: i32 = BF - 1;

fn ref_eq() -> ValType {
    ValType::Ref {
        nullable: false,
        heap: HeapType::Eq,
    }
}

fn ref_eq_null() -> ValType {
    ValType::Ref {
        nullable: true,
        heap: HeapType::Eq,
    }
}

const PV_LEN: u32 = 0;
const PV_SHIFT: u32 = 1;
const PV_ROOT: u32 = 2;
const PV_TAIL: u32 = 3;

const VI_CHILDREN: u32 = 0;
const VI_SIZES: u32 = 1;

pub fn make() -> ModuleIR {
    let mut m = ModuleIR::new("rt.arr");

    m.globals.push(GlobalDef {
        name: "empty_leaf".into(),
        mutable: false,
        ty: ref_array(),
        init: vec![Instr::ArrayNewFixed(T_ARRAY.into(), 0)],
    });
    m.globals.push(GlobalDef {
        name: "empty_pvec".into(),
        mutable: false,
        ty: ref_pvec(),
        init: vec![
            Instr::I32Const(0),
            Instr::I32Const(0),
            Instr::RefNull(HeapType::Named(T_VEC_INTERNAL.into())),
            Instr::GlobalGet("empty_leaf".into()),
            Instr::StructNew(T_PVEC.into()),
        ],
    });

    let funcs = vec![
        tailoff_fn(),
        vi_nav_fn(),
        child_count_fn(),
        copy_leaves_fn(),
        node_total_size_fn(),
        make_relaxed_fn(),
        pack_children_fn(),
        append_centre_fn(),
        concat_trees_fn(),
        wrap_leaf_fn(),
        get_leaf_fn(),
        new_path_fn(),
        push_tail_fn(),
        do_set_fn(),
        push_fn(),
        promote_full_tail_fn(),
        make_fn(),
        get_fn(),
        set_fn(),
        set_in_place_fn(),
        len_fn(),
        concat_fn(),
        slice_trim_right_fn(),
        slice_trim_left_fn(),
        slice_last_leaf_fn(),
        slice_collapse_root_fn(),
        slice_from_full_tree_fn(),
        slice_fn(),
        pop_tail_fn(),
        drop_last_fn(),
        builder_new_fn(),
        builder_from_fn(),
        builder_push_fn(),
        builder_append_leaf_range_fn(),
        builder_extend_node_fn(),
        builder_extend_fn(),
        builder_freeze_fn(),
        from_array_fn(),
        to_array_fn(),
        from_read_file_result_fn(),
        make_empty_fn(),
    ];

    for f in funcs {
        m.exports.push(ExportDef {
            wasm_name: f.name.clone(),
            func_sym: f.name.clone(),
        });
        m.funcs.push(f);
    }

    m
}
