#!/bin/sh
# Regenerate the Twinkle test aggregators from the days present on disk.
#
# Twinkle has no test auto-discovery, so the suite files are generated: for each
# y<YEAR>/ directory we emit a library `suite.tw` (exports `all()`) and a
# runnable `main.tw`; the repo-root `main.tw` concatenates every year's `all()`.
#
# Run from the examples/aoc root (the Makefile pins cwd there).
set -eu

GEN_NOTE="// GENERATED by \`make gen\` — do not edit by hand."

years=""
for ydir in y*/; do
  [ -d "$ydir" ] || continue
  year=${ydir%/}
  years="$years $year"

  days=""
  for ddir in "$year"/day*/; do
    [ -d "$ddir" ] || continue
    [ -f "$ddir/solution.tw" ] || continue
    day=$(basename "$ddir")
    days="$days $day"
  done

  # y<YEAR>/suite.tw — library: imports each day, exports all() suites.
  {
    echo "$GEN_NOTE"
    echo "// Aggregates every day's test suite for ${year#y}. Library only (no top-level"
    echo "// run), so the all-years runner can import it without triggering a run."
    echo ""
    echo "use runner"
    for day in $days; do
      echo "use .$day.solution as $day"
    done
    echo ""
    echo "pub fn all() Vector<runner.Suite> {"
    echo "  ["
    for day in $days; do
      echo "    $day.suite(),"
    done
    echo "  ]"
    echo "}"
  } > "$year/suite.tw"

  # y<YEAR>/main.tw — runnable: run just this year.
  {
    echo "$GEN_NOTE"
    echo "// Runnable entry for the whole year: \`make test YEAR=${year#y}\`."
    echo ""
    echo "use runner"
    echo "use .suite"
    echo ""
    echo "runner.run_all(suite.all())"
  } > "$year/main.tw"
done

# Repo-root main.tw — runnable: concatenate every year's suites.
{
  echo "$GEN_NOTE"
  echo "// Runnable entry for every year: \`make test\`."
  echo ""
  echo "use runner"
  for year in $years; do
    echo "use $year.suite as $year"
  done
  echo ""
  if [ -z "$years" ]; then
    echo "runner.run_all([])"
  else
    expr=""
    for year in $years; do
      if [ -z "$expr" ]; then
        expr="$year.all()"
      else
        expr="$expr.concat($year.all())"
      fi
    done
    echo "runner.run_all($expr)"
  fi
} > main.tw

echo "gen: wrote aggregators for years:${years:- (none)}"
